DROP DATABASE SGENO;
CREATE DATABASE SGENO;
USE SGENO;

CREATE TABLE ALUNO (
MATRICULA VARCHAR(10) PRIMARY KEY NOT NULL,
NOME VARCHAR(50) NOT NULL,
CURSO CHAR(1) NOT NULL,
FASE INT NOT NULL,
SEXO CHAR NOT NULL,
TELEFONE VARCHAR(15) NOT NULL,
CELULAR VARCHAR(15),
EMAIL VARCHAR(50),
ALUNO_STATUS CHAR DEFAULT 'A' NOT NULL 
);

CREATE TABLE EMPRESA (
COD_EMPRESA INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
NOME VARCHAR(100) NOT NULL,
ENDERECO VARCHAR(100) NOT NULL,
RESPONSAVEL VARCHAR(100),
TELEFONE VARCHAR(15) NOT NULL,
EMAIL VARCHAR(50),
EMPRESA_STATUS CHAR DEFAULT 'A' NOT NULL
);


CREATE TABLE CONTRATO (
COD_CONTRATO INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
COD_ALUNO INT NOT NULL,
COD_EMPRESA INT NOT NULL,
COD_VAGA INT NOT NULL,
PERIODODE VARCHAR(8) NOT NULL,
PERIODOATE VARCHAR(8) NOT NULL,
COD_ADITIVO INT,
COD_AGENCIA INT,
OBSERVCONTRATO BLOB,
CONTRATO_STATUS CHAR DEFAULT 'A' NOT NULL
);


CREATE TABLE VAGA (
COD_VAGA INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
DESC_VAGA VARCHAR(25) NOT NULL,
COD_EMPRESA INT NOT NULL,
COD_CURSO INT NOT NULL,
FASEMIN INT NOT NULL,
VALOR INT NOT NULL,
TURNO INT  NOT NULL,
HORADE TIME NOT NULL,
HORAATE TIME NOT NULL,
CARGAHORARIA INT NOT NULL,
OBSERVVAGA BLOB,
VAGA_STATUS CHAR DEFAULT 'A' NOT NULL
);
	
CREATE TABLE ADITIVO (
COD_ADITIVO INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
DESC_ADITIVO VARCHAR(100),
STATUS_ADITIVO CHAR(1)
